/**
 * WordPress migration acceptance test (issue #1691).
 *
 * Drives the runtime WXR import pipeline — the same code path the admin
 * import wizard uses — end to end against a realistic committed export
 * fixture (`fixtures/realistic-export.xml`), then asserts on the resulting
 * database state:
 *
 *   parse WXR → taxonomy plan → importContent → sections → media import
 *   (network stubbed) → URL rewriting
 *
 * What this covers: posts, pages, a custom post type, drafts/pending,
 * preserved slugs and dates, Gutenberg → Portable Text (incl. classic
 * HTML), hierarchical categories, tags, a custom taxonomy, featured
 * images, media import with URL rewriting into content, bylines from WXR
 * authors, reusable blocks as sections, and idempotent re-runs.
 *
 * What this deliberately does NOT cover (tested elsewhere / other scope):
 *
 * - Custom-field and SEO *values* (ACF/Yoast meta): the WXR runtime path
 *   surfaces them in the analyze step but does not write them; value
 *   import is the plugin importer's job — see
 *   `wordpress-import/plugin-execute-fields.test.ts`. The fixture still
 *   carries the meta so the parser-visibility of it is pinned here.
 * - Comments and menus: plugin importer scope — see
 *   `wordpress-import/plugin-execute-chunked.test.ts`.
 * - The redirects map: generated by the CLI two-phase import — see
 *   `wordpress-import/wordpress-import.test.ts` ("creates redirects map").
 * - Multilingual (WPML/Polylang) imports: see
 *   `wordpress-import/wxr-i18n-taxonomies.test.ts`.
 */

